const numbers = [1, 5, 8, 12, 3, 15, 7, 20];
const words = ["–∫—ñ—Ç", "—Å–æ–±–∞–∫–∞", "–º–∏—à–∞", "–ø–∞–ø—É–≥–∞", "—Ö–æ–º—è–∫"];

function countItems(array, condition) {
  let count = 0;

  for (const element of array) {
    if (condition(element)) {
      count++;
    }
  }

  return count;
}

