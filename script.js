const isEven = (num) => num % 2 === 0;
const isLarge = (num) => num > 10;
const isShort = (word) => word.length <= 3;

// –¢–µ—Å—Ç—É–≤–∞–Ω–Ω—è
console.log("–ü–∞—Ä–Ω–∏—Ö —á–∏—Å–µ–ª:", countItems(numbers, isEven)); // 3
